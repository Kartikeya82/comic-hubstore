// Helper functions
export function getComicById(id) {
  return COMICS.find((comic) => comic.id === id);
}

export function getNewReleases() {
  return COMICS.filter((comic) => comic.isNew).slice(0, 6);
}

export function getPopular() {
  return COMICS.filter((comic) => comic.isPopular).slice(0, 6);
}

export function getByPublisher(publisher) {
  return COMICS.filter((comic) => comic.publisher === publisher);
}

export function getByGenre(genre) {
  return COMICS.filter((comic) => comic.genre === genre);
}

export function searchComics(query) {
  const lowerQuery = query.toLowerCase();
  return COMICS.filter(
    (comic) =>
      comic.title.toLowerCase().includes(lowerQuery) ||
      comic.series.toLowerCase().includes(lowerQuery) ||
      comic.characters.some((char) => char.toLowerCase().includes(lowerQuery))
  );
}

export function getPublishers() {
  return [...new Set(COMICS.map((comic) => comic.publisher))];
}

export function getGenres() {
  return [...new Set(COMICS.map((comic) => comic.genre))];
}
